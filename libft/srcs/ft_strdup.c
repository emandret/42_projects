#include "libft.h"

char *ft_strdup(const char *s)
{
    size_t len;
    char  *dup;

    len = ft_strlen(s);
    if (!(dup = ft_strnew(len)))
        return (NULL);
    dup = ft_strcpy(dup, s);
    return (dup);
}

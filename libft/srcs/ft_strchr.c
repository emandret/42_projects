#include "libft.h"

char *ft_strchr(const char *s, int c)
{
    const char  *str;
    unsigned int i;

    str = (char *) s;
    i   = 0;
    while (str[i] != (char) c)
    {
        if (str[i] == '\0')
            return (NULL);
        i++;
    }
    return ((char *) str + i);
}
